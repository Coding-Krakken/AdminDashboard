#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";

const [, , command, ...args] = process.argv;

function parseArgs(argv) {
  return argv.reduce((acc, value) => {
    const [key, raw] = value.split("=");
    if (key.startsWith("--")) {
      acc[key.slice(2)] = raw ?? "true";
    }
    return acc;
  }, {});
}

function ensureDir(dirPath) {
  if (!fs.existsSync(dirPath)) {
    fs.mkdirSync(dirPath, { recursive: true });
  }
}

function signPlugin(plugin, secret) {
  const payload = JSON.stringify({
    id: plugin.id,
    version: plugin.version,
    manifest: plugin.manifest
  });

  return crypto.createHmac("sha256", secret).update(payload).digest("hex");
}

function createModuleScaffold(options) {
  const moduleId = options.id;
  if (!moduleId) {
    console.error("Missing --id option. Example: universal-admin create-module --id=inventory --title=Inventory");
    process.exit(1);
  }

  const title = options.title ?? moduleId;
  const route = options.route ?? `/${moduleId}`;
  const category = options.category ?? "custom";
  const permission = options.permission ?? `${moduleId}:read`;
  const signingSecret = options.secret ?? "starter-signing-secret-v1";

  const targetDir = path.resolve(process.cwd(), "apps/starter-next/src/platform/generated", moduleId);
  ensureDir(targetDir);

  const manifestPath = path.join(targetDir, "manifest.json");
  const readmePath = path.join(targetDir, "README.md");
  const settingsPath = path.join(targetDir, "settings.schema.json");

  const manifest = {
    id: `${moduleId}-plugin`,
    version: "0.1.0",
    source: "generated",
    manifest: {
      id: moduleId,
      title,
      route,
      category,
      order: 999,
      requiredPermissions: [permission]
    }
  };

  manifest.signature = signPlugin(manifest, signingSecret);

  const settingsSchema = {
    moduleId,
    schema: {
      type: "object",
      additionalProperties: false,
      properties: {
        enabled: { type: "boolean", default: true },
        refreshIntervalSeconds: { type: "integer", minimum: 15, maximum: 3600, default: 60 }
      },
      required: ["enabled"]
    }
  };

  fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`, "utf8");
  fs.writeFileSync(
    readmePath,
    `# ${title}\n\nGenerated by universal-admin CLI.\n\n- Route: ${route}\n- Category: ${category}\n- Permission: ${permission}\n- Plugin ID: ${manifest.id}\n- Signature: ${manifest.signature}\n`,
    "utf8"
  );
  fs.writeFileSync(settingsPath, `${JSON.stringify(settingsSchema, null, 2)}\n`, "utf8");

  console.log(`Generated module scaffold at ${targetDir}`);
}

if (command === "create-module") {
  const options = parseArgs(args);
  createModuleScaffold(options);
} else {
  console.log("Universal Admin CLI");
  console.log("Commands:");
  console.log("  create-module --id=<id> [--title=Title] [--route=/path] [--category=custom] [--permission=entity:read] [--secret=<signing-secret>]");
}
